<template>
  <div :class="{'zl-title': true, 'first': this.first, 'no-first': !this.first}">
    <section :class="sectionClass"></section>
    <slot></slot>
  </div>
</template>

<script>
export default {
  name: 'ZlTitle',
  props: {
    first: {
      type: Boolean,
      default: false,
    },
    section: {
      type: Boolean,
      default: false,
    },
  },
  computed: {
    sectionClass() {
      return this.section ? 'section show-section' : 'section hide-section';
    },
    titleClass() {
      return this.first ? 'zl-title first' : 'zl-title no-first';
    },
  },
};
</script>

